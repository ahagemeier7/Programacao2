<?php

class Calculadora{

    public function soma($n1, $n2){
        echo $n1 + $n2;
    }
    public function subtracao($n1, $n2){
        echo $n1 - $n2;
    }

    public function divisao($n1, $n2){
        echo $n1 / $n2;
    }

    public function multiplicacao( $n1, $n2 ){
        echo $n1 * $n2;
    }
}

?>